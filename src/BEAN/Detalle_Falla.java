package BEAN;

public class Detalle_Falla {
    private int orden_servicio;
    private int id_falla;

    public Detalle_Falla() {}

    public int getOrden_servicio() {
        return orden_servicio;
    }

    public void setOrden_servicio(int orden_servicio) {
        this.orden_servicio = orden_servicio;
    }

    public int getId_falla() {
        return id_falla;
    }

    public void setId_falla(int id_falla) {
        this.id_falla = id_falla;
    }
}
